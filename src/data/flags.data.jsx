export const normalizeLorenz = (lorenz) =>
  lorenz.map((series) => {
    const total = series.data?.[series.data.length - 1]?.y ?? 0;

    return {
      ...series,
      data: series.data.map((p) => ({
        x: p.x,
        y: total ? p.y / total : 0,
      })),
    };
  });

